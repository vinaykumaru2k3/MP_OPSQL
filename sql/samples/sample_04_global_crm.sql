-- Sample Global CRM Schema 
-- Designed for SchemaForge Sandbox and Validation Simulation Testing

-- ---------------------------------------------------------
-- CRM Core Entities
-- ---------------------------------------------------------

CREATE TABLE CRM_ACCOUNTS (
    ACCOUNT_ID NUMBER(12,0) PRIMARY KEY,
    ACCOUNT_NAME VARCHAR2(150) NOT NULL,
    INDUSTRY VARCHAR2(100),
    ANNUAL_REVENUE NUMBER(18,2),
    EMPLOYEES NUMBER(8,0),
    ACCOUNT_OWNER_ID NUMBER(10,0),
    LATEST_ACTIVITY DATE DEFAULT SYSDATE,
    INTERNAL_NOTES CLOB,
    SYS_GUID RAW(16) DEFAULT SYS_GUID()
);

CREATE TABLE CRM_CONTACTS (
    CONTACT_ID NUMBER(12,0) PRIMARY KEY,
    ACCOUNT_ID NUMBER(12,0) REFERENCES CRM_ACCOUNTS(ACCOUNT_ID),
    FIRST_NAME VARCHAR2(50),
    LAST_NAME NVARCHAR2(50) NOT NULL,
    EMAIL VARCHAR2(150),
    PHONE VARCHAR2(20),
    TITLE VARCHAR2(100),
    PHOTO_ID BLOB,
    IS_PRIMARY NUMBER(1) DEFAULT 0
);

CREATE TABLE CRM_OPPORTUNITIES (
    OPP_ID NUMBER(12,0) PRIMARY KEY,
    ACCOUNT_ID NUMBER(12,0) REFERENCES CRM_ACCOUNTS(ACCOUNT_ID),
    OPP_NAME VARCHAR2(150) NOT NULL,
    STAGE VARCHAR2(50),
    PROBABILITY NUMBER(3,0),
    AMOUNT NUMBER(15,2),
    CLOSE_DATE DATE,
    FORECAST_XML XMLTYPE
);

-- ---------------------------------------------------------
-- CRM Activity & Interaction
-- ---------------------------------------------------------

CREATE TABLE CRM_TASKS (
    TASK_ID NUMBER(15,0) PRIMARY KEY,
    RELATED_TO_ID NUMBER(12,0),
    RELATED_TYPE VARCHAR2(50), -- e.g., 'ACCOUNT', 'CONTACT', 'OPPORTUNITY'
    SUBJECT VARCHAR2(200) NOT NULL,
    DUE_DATE DATE,
    STATUS VARCHAR2(30) DEFAULT 'Not Started',
    PRIORITY VARCHAR2(20) DEFAULT 'Normal'
);

CREATE TABLE CRM_COMMUNICATIONS (
    COMM_ID NUMBER(15,0) PRIMARY KEY,
    CONTACT_ID NUMBER(12,0) REFERENCES CRM_CONTACTS(CONTACT_ID),
    COMM_TYPE VARCHAR2(50), -- 'EMAIL', 'CALL', 'MEETING'
    COMM_DATE DATE DEFAULT SYSDATE,
    DURATION_MINUTES NUMBER(4,0),
    NOTES CLOB
);

CREATE TABLE CRM_LEADS (
    LEAD_ID NUMBER(12,0) PRIMARY KEY,
    FIRST_NAME VARCHAR2(50),
    LAST_NAME VARCHAR2(50) NOT NULL,
    COMPANY VARCHAR2(100),
    EMAIL VARCHAR2(150),
    STATUS VARCHAR2(50),
    SCORE NUMBER(3,0)
);

CREATE TABLE CRM_CAMPAIGNS (
    CAMPAIGN_ID NUMBER(10,0) PRIMARY KEY,
    NAME VARCHAR2(150),
    TYPE VARCHAR2(50),
    STATUS VARCHAR2(30),
    START_DATE DATE,
    END_DATE DATE,
    BUDGET_ALLOCATED NUMBER(12,2),
    ROI_METRICS XMLTYPE
);

CREATE TABLE CRM_CAMPAIGN_MEMBERS (
    MEMBER_ID NUMBER(15,0) PRIMARY KEY,
    CAMPAIGN_ID NUMBER(10,0) REFERENCES CRM_CAMPAIGNS(CAMPAIGN_ID),
    LEAD_ID NUMBER(12,0) REFERENCES CRM_LEADS(LEAD_ID),
    CONTACT_ID NUMBER(12,0) REFERENCES CRM_CONTACTS(CONTACT_ID),
    STATUS VARCHAR2(50)
);

-- ---------------------------------------------------------
-- Sequences & Advanced Operations
-- ---------------------------------------------------------

CREATE SEQUENCE CRM_ACCT_SEQ START WITH 1 INCREMENT BY 1;
CREATE SEQUENCE CRM_CONT_SEQ START WITH 1 INCREMENT BY 1;

-- Specific NVL, LNNVL usage
SELECT ACCOUNT_NAME 
FROM CRM_ACCOUNTS 
WHERE LNNVL(ANNUAL_REVENUE > 1000000);

SELECT FIRST_NAME, NVL(TITLE, 'No Title Provided') 
FROM CRM_CONTACTS;

-- PL/SQL Bulk Processing logic simulation
DECLARE
    TYPE OppArray IS TABLE OF CRM_OPPORTUNITIES%ROWTYPE;
    v_opps OppArray;
BEGIN
    SELECT * BULK COLLECT INTO v_opps FROM CRM_OPPORTUNITIES WHERE STAGE = 'Closed Won';
    FORALL i IN 1..v_opps.COUNT
        INSERT INTO CRM_OPPORTUNITY_HISTORY VALUES v_opps(i);
END;

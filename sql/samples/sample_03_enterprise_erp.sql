-- Sample Enterprise ERP Schema - High Complexity
-- Designed for SchemaForge Sandbox and Validation Simulation Testing

-- ---------------------------------------------------------
-- Core ERP Tables
-- ---------------------------------------------------------

CREATE TABLE ERP_COMPANY (
    COMPANY_ID NUMBER(10,0) PRIMARY KEY,
    COMPANY_NAME VARCHAR2(100) NOT NULL,
    REGISTRATION_CODE VARCHAR2(50),
    FOUNDED_DATE DATE DEFAULT SYSDATE,
    GLOBAL_REGION NVARCHAR2(50),
    STATUS NUMBER(1) DEFAULT 1
);

CREATE TABLE ERP_LOCATIONS (
    LOCATION_ID NUMBER(10,0) PRIMARY KEY,
    COMPANY_ID NUMBER(10,0) REFERENCES ERP_COMPANY(COMPANY_ID),
    ADDRESS_LINE_1 VARCHAR2(255),
    POSTAL_CODE VARCHAR2(20),
    COUNTRY_CD VARCHAR2(3),
    LAT_LONG_COORD ROWID,
    COMMENTS CLOB
);

CREATE TABLE ERP_DEPARTMENTS (
    DEPT_ID NUMBER(10,0) PRIMARY KEY,
    LOCATION_ID NUMBER(10,0) REFERENCES ERP_LOCATIONS(LOCATION_ID),
    DEPT_NAME VARCHAR2(100) NOT NULL,
    MANAGER_ID NUMBER(10,0),
    BUDGET_XML XMLTYPE
);

CREATE TABLE ERP_EMPLOYEES (
    EMP_ID NUMBER(10,0) PRIMARY KEY,
    DEPT_ID NUMBER(10,0) REFERENCES ERP_DEPARTMENTS(DEPT_ID),
    FIRST_NAME VARCHAR2(50) NOT NULL,
    LAST_NAME NVARCHAR2(50) NOT NULL,
    HIRE_DATE DATE,
    SALARY NUMBER(15,2),
    COMMISSION_PCT NUMBER(5,2),
    EMP_PROFILE_IMG BLOB,
    SYS_IDENTIFIER RAW(16) DEFAULT SYS_GUID()
);

-- ---------------------------------------------------------
-- Finance & Audit Tables
-- ---------------------------------------------------------

CREATE TABLE ERP_INVOICES (
    INVOICE_ID NUMBER(15,0) PRIMARY KEY,
    COMPANY_ID NUMBER(10,0),
    ISSUED_DATE DATE DEFAULT SYSDATE,
    DUE_DATE DATE,
    TOTAL_AMOUNT NUMBER(19,4),
    PAYMENT_STATUS VARCHAR2(20) DEFAULT 'PENDING'
);

CREATE TABLE ERP_PAYMENTS (
    PAYMENT_ID NUMBER(15,0) PRIMARY KEY,
    INVOICE_ID NUMBER(15,0) REFERENCES ERP_INVOICES(INVOICE_ID),
    PAYMENT_DATE DATE,
    AMOUNT_PAID NUMBER(19,4),
    TXN_RECEIPT BLOB
);

CREATE TABLE ERP_GL_ACCOUNTS (
    ACct_ID NUMBER(10,0) PRIMARY KEY,
    ACCOUNT_NAME VARCHAR2(100) NOT NULL,
    ACCOUNT_TYPE VARCHAR2(50),
    BALANCE NUMBER(19,4)
);

CREATE TABLE ERP_TRANSACTIONS (
    TXN_ID NUMBER(20,0) PRIMARY KEY,
    ACCT_ID NUMBER(10,0) REFERENCES ERP_GL_ACCOUNTS(ACCT_ID),
    TXN_DATE DATE DEFAULT SYSDATE,
    DEBIT_CREDIT CHAR(1),
    AMOUNT NUMBER(19,4)
);

-- ---------------------------------------------------------
-- Supply Chain
-- ---------------------------------------------------------

CREATE TABLE SC_VENDORS (
    VENDOR_ID NUMBER(10,0) PRIMARY KEY,
    NAME VARCHAR2(200),
    EVALUATION_METRICS XMLTYPE,
    CONTACT_INFO CLOB
);

CREATE TABLE SC_PRODUCTS (
    PRODUCT_ID NUMBER(10,0) PRIMARY KEY,
    VENDOR_ID NUMBER(10,0) REFERENCES SC_VENDORS(VENDOR_ID),
    PRODUCT_NAME VARCHAR2(150),
    PRICE NUMBER(12,2),
    STOCK_LEVEL NUMBER(10,0)
);

CREATE TABLE SC_WAREHOUSES (
    WAREHOUSE_ID NUMBER(10,0) PRIMARY KEY,
    LOCATION_ID NUMBER(10,0),
    CAPACITY NUMBER(15,0)
);

CREATE TABLE SC_INVENTORY (
    INV_ID NUMBER(15,0) PRIMARY KEY,
    PRODUCT_ID NUMBER(10,0) REFERENCES SC_PRODUCTS(PRODUCT_ID),
    WAREHOUSE_ID NUMBER(10,0) REFERENCES SC_WAREHOUSES(WAREHOUSE_ID),
    QTY_ON_HAND NUMBER(10,0),
    LAST_AUDIT DATE
);

-- ---------------------------------------------------------
-- Sequences & Triggers
-- ---------------------------------------------------------

CREATE SEQUENCE emp_seq START WITH 1000 INCREMENT BY 1;
CREATE SEQUENCE inv_seq START WITH 50000;

-- ---------------------------------------------------------
-- Oracle-Specific Queries & PL/SQL Blocks
-- ---------------------------------------------------------

SELECT 
    NVL(FIRST_NAME, 'N/A') AS FNAME,
    NVL2(COMMISSION_PCT, SALARY + (SALARY * COMMISSION_PCT), SALARY) AS TOTAL_COMP,
    DECODE(STATUS, 1, 'Active', 0, 'Inactive', 'Unknown')
FROM ERP_COMPANY c, ERP_EMPLOYEES e
WHERE c.COMPANY_ID = 1;

-- Recursive Connect By
SELECT EMP_ID, MANAGER_ID, LEVEL 
FROM ERP_EMPLOYEES 
START WITH MANAGER_ID IS NULL 
CONNECT BY PRIOR EMP_ID = MANAGER_ID;

-- Outer joins
SELECT e.FIRST_NAME, d.DEPT_NAME 
FROM ERP_EMPLOYEES e, ERP_DEPARTMENTS d 
WHERE e.DEPT_ID = d.DEPT_ID (+);

-- Set Operators
SELECT ACCOUNT_NAME FROM ERP_GL_ACCOUNTS MINUS SELECT ACCOUNT_NAME FROM ERP_GL_ACCOUNTS WHERE BALANCE < 0;

-- PL/SQL Autonomous Transaction
DECLARE 
    PRAGMA AUTONOMOUS_TRANSACTION;
    v_total NUMBER;
BEGIN 
    SELECT SUM(AMOUNT) INTO v_total FROM ERP_TRANSACTIONS;
    COMMIT;
END;

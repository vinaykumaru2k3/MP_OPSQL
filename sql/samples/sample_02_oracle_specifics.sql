-- Constructs tested: VARCHAR2, NUMBER, DATE, CLOB, BLOB, ROWID, XMLTYPE
CREATE TABLE EMPLOYEES (
    EMP_ID NUMBER(10,0) PRIMARY KEY,
    FIRST_NAME VARCHAR2(50) NOT NULL,
    LAST_NAME NVARCHAR2(50),
    HIRE_DATE DATE,
    EMP_RECORD ROWID,
    KPI_DATA XMLTYPE,
    BIOGRAPHY CLOB,
    PROFILE_PIC BLOB,
    IS_ACTIVE NUMBER(1)
);

-- Constructs tested: .NEXTVAL sequence usage
CREATE SEQUENCE emp_seq START WITH 1;
INSERT INTO EMPLOYEES (EMP_ID) VALUES (emp_seq.NEXTVAL);

-- Constructs tested: NVL, NVL2, DECODE, SYSDATE, SYS_GUID()

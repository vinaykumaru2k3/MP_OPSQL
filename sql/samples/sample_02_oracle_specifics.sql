    NVL(FIRST_NAME, 'Unknown'),
    NVL2(LAST_NAME, LAST_NAME, 'No Last Name'),
    DECODE(IS_ACTIVE, 1, 'Active', 'Inactive'),
    SYSDATE,
    SYS_GUID()
FROM EMPLOYEES;

-- Constructs tested: LNNVL function
